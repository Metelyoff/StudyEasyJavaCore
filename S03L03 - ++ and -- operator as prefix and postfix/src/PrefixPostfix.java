
public class PrefixPostfix {

	public static void main(String[] args) {
		int var=5;  int var2 = 6;  
		
        System.out.println(var++);          
        System.out.println( ++var);
        System.out.println("--------------------------------------------");
        System.out.println(var2--);          
        System.out.println( --var2);

	}

}

